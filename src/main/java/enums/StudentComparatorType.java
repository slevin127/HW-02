package enums;

/**
 * Перечисление доступных типов компараторов для сортировки студентов.
 */
public enum StudentComparatorType {
    /** Сортировка по полному имени. */
    FULL_NAME,
    /** Сортировка по идентификатору университета. */
    UNIVERSITY_ID,
    /** Сортировка по номеру текущего курса. */
    CURRENT_COURSE_NUMBER,
    /** Сортировка по среднему баллу экзаменов. */
    AVG_EXAM_SCORE
}
